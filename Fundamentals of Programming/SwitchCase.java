import java.util.*;
public class SwitchCase {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int number=sc.nextInt();

        switch (number) {
            case 1 : System.out.println("Momos"); break ;
            case 2 : System.out.println("Pizza"); break ;
            case 3 : System.out.println("Burger"); break ;
            case 4 : System.out.println("Pasta");

            default : System.out.println("AM I ASKING FOR TOO MUCH?");
        }

    
}
    
}
